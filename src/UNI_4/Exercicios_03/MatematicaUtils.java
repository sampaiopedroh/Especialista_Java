package UNI_4.Exercicios_03;

public class MatematicaUtils {
    static double calcularAreaQuadrado(final double medidaLado) {
        return medidaLado * medidaLado;
    }

    static  double calcularAreaCirculoComRaio(final double medidaRadio) {
        return (medidaRadio * medidaRadio) * 3.14159265358979323846;
    }
}
