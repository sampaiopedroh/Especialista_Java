package UNI_8.ArraysMultidimencionais;

public class Principal3 {
    public static void main(String[] args) {
        String[][] cidades = new String[2][];
        cidades[0] = new String[3];
        cidades[0][2] = "São Paulo";

        cidades[1] = new String[2];
        cidades[1][1] = "Uberaba";

        cidades[2] = new String[1];
        cidades[2][0] = "Uberlândia";
    }
}
