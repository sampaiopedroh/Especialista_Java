package UNI_2;

public class ConversaoIntParaLong {
	public static void main(String[] args) {
		// De um tipo de capacidade menor para um de capacidade maior, ocorre um casting automático (sendo a instrucão de casting opcional)
		int y = 102_344;
		long x = y;
		System.out.println(y);
		System.out.println(x);
	}
}
